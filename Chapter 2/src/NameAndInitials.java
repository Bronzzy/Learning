public class NameAndInitials {
    public static void main(String[] args) {

        String firstName = "Dao";
        String middleName = "Hoa";
        String lastName = "Binh";

        char firstInitial = 'D';
        char middleInitial = 'H';
        char lastInitial = 'B';

        System.out.println(firstName + " " + middleName + " " + lastName);
        System.out.println(firstInitial + " " +middleInitial+ " " +lastInitial);

    }
}
